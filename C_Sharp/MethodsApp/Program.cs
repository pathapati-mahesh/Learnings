Console.WriteLine("Hello, World!");
for (int i = 0; i < 5; i++)
{
    Console.WriteLine($"Iteration {i + 1}");
}
for (int j = 5; j > 0; j--)
{
    Console.WriteLine($"Countdown {j}");
}
for (int k = 0; k <= 10; k += 2)
{
    Console.WriteLine($"Even Number: {k}");
}
for (int m = 10; m >= 0; m -= 2)
{
    Console.WriteLine($"Even Countdown: {m}");
}